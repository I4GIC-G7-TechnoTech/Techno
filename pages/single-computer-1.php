            <h4><b>3. Razer Blade Stealth</b></h4>
                <p><i>CPU: Intel Core i7 | Graphics: Intel HD Graphics 620 | RAM: 8GB – 16GB | Screen: 12.5-inch, QHD+ (2,560 x 1,440) – 4K (3,840 x 2,160) IGZO LED-backlit multi-touch | Storage: 128GB – 1TB SSD</i>
                <hr>
               The Razer Blade Stealth is an exceptional Ultrabook hindered only by its efforts in trying to be a gaming laptop. Price-wise, it has the upperhand against key competitors, but don’t be fooled – with an Intel Kaby Lake Core i7 processor, the latest Blade Stealth is more powerful and power-efficient than ever. Better yet, this laptop can change the lighting of each key on its keyboard, with more than 16.8 million colors to choose from. Plus, if you mind the integrated graphics from Intel, you can attach a (albeit rather pricey) Razer Core external GPU enclosure for boosted performance when stationary.</p>
