                <h4><b>4. Asus Chromebook Flip</b></h4>
                <p><i>CPU: Intel Pentium – Core m3 | Graphics: Intel HD Graphics 515 | RAM: 4GB | Screen: 12.5-inch, FHD (1,920 x 1,080) LED backlit anti-glare | Storage: 32GB – 64GB eMMC</i>
                <hr>
                When Asus first came out with the Chromebook Flip as a “premium” offering, we were skeptical. Premium Chromebooks had been done before with little to no fanfare. After all, who would want to spend over a grand on a laptop that’s limited to the Chrome browser and a handful of Android apps? This concept becomes more enticing when you cut the price in half, as Asus has skillfully accomplished with the latest Chromebook Flip. Stacked with a gorgeous design, a keyboard that feels rich to the touch and even a 2-in-1 form factor, the Asus Chromebook Flip proves that Chromebooks can be premium without going overboard.</p>
