            <h4><b>1. Dell XPS 13</b></h4>
                <p>
                <i>CPU: Intel Core i3 – i7 | Graphics: Intel HD Graphics 620 | Screen: 13.3-inch FHD (1,920 x 1,080) – QHD+ (3,200 x 1,800) | Storage: 128GB – 512GB SSD</i>
                <hr>
                With the 2016 model, the Dell XPS 13 reigns supreme as the best laptop you can buy today. Thin and light with a battery life that exceeds 7 hours, according to our movie test, Dell’s flagship laptop is the posterchild for Ultrabooks. Once again, too, Dell has managed to squeeze a 13.3-inch screen into an 11-inch frame, proving the nigh-borderless InfinityEdge display to be a design marvel. Outfitted with Intel’s latest Kaby Lake processors and lightning-fast storage and memory, the Dell XPS 13 is dressed to impress with welcome addition of a Rose Gold color option as well. It should comes as no surprise, then, that we still rank it as the best Ultrabook and best laptop overall.</p>
